package calculatrice;

/**
 * La classe Calculatrice est dédiée à la mise en oeuvre de méthodes de
 * résolution mathématique
 * 
 * @author Etienne Cassin
 */

public class Calculatrice {
    /**
     * La méthode "plus" réalise la somme de deux chiffres
     * 
     * @param a Le premier chiffre que l'on va additionner
     * @param b Le second chiffre que l'on va additionner
     * @return La somme de a et b
     */
    public int plus(int a, int b) {
        return a + b;
    }

    public int moins(int a, int b) {
        return a - b;
    }
}
